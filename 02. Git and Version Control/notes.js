// Commands :-

// 1) pwd :- gives the location of the print working directory

// 2) ls :- lists all the files in present working directory

// 3) CTRL + L :- To scroll up the entire screen

// 4) mkdir "folder_name" :- To create a folder in present working directory
// (In Windows use CTRL + Shift + N)

// 5) touch file_names :- To create files in the current working directory

// e.g :- touch index.html style.css script.js

// 6) mv file_names new_location_name :- to move files from current location to other

// e.g :- mv index.html style.css script.js ./webdev



// 7) Renaming folders :-
// Command :- mv old_folder new_folder

// e.g :- folder1 --> folder2
//  mv folder1 folder_new





